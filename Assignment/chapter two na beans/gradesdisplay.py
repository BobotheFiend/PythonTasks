mark = int(input("Enter a number from 1-100: "))

if mark >= 90 and mark <= 100:
    print(mark,' = ','Your grade is A')
if mark >= 80 and mark <= 89:
    print(mark,' = ','Your grade is B')
if mark >= 70 and mark <= 79:
    print(mark,' = ','Your grade is C')
if mark >= 60 and mark <= 69:
    print(mark,' = ','Your grade is D')
if mark >= 0 and mark <= 60:
    print(mark,' = ','Your grade is F')

