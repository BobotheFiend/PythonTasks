#Generates rainbow colours from 1-7 randomly

import random

number = random.randint(1, 7)

if number == 1:
    print(f" Violet is {number}")
elif number == 2:
    print(f" Blue is {number}")
elif number == 3:
    print(f" Orange is {number}")
elif number == 4:
    print(f" Green is {number}")
elif number == 5:
    print(f" Yellow is {number}")
elif number == 6:
    print(f" Red is {number}")
elif number == 7:
    print(f" Indigo is {number}")
