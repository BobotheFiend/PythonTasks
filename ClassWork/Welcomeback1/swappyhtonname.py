def namePYTHON(name):
    if (name == "P"):
        return "p"
    if (name == "y"):
        return "Y"
    if (name == "T"):
        return "t"
    if (name == "h"):
        return "H"
    if (name == "O"):
        return "o"
    if (name == "n"):
        return "N"

name = "PyThOn"
print(namePYTHON(name))
